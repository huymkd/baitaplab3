package baitaplab3;

public class Point2d {

    private Float x = 0.0f;
    private Float y = 0.0f;

    public Point2d() {
    }

    public Point2d(Float x, Float y) {
        this.x = x;
        this.y = y;
    }

    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }

}
